public class Arithmetic {
    public static void main(String[] args) {
        int p=60, q=90;
        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
        System.out.println(q%p);
        System.out.println(p++ + ++q);
        System.out.println(p-- + --q);

    }
}
